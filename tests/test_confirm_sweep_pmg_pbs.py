def test_remote_create_autogen_response_reaches_caller_but_never_ledger(tmp_path, monkeypatch):
    """THE UNIQUE THIRD CASE: when encryption_key='autogen', PBS may return a server-generated
    encryption-key in the CREATE response itself (config.encryption-key). That value MUST reach
    the caller (there is no second copy) but MUST NEVER be recorded to the ledger — the
    TFA-recovery-code "secret in a mutation's own RETURN" precedent, applied proactively here."""
    _, _, _, log = _wire(
        tmp_path, monkeypatch,
        remote_create_return={"remote": "r1", "config": {"encryption-key": _AUTOGEN_RETURN_SENTINEL}},
    )

    out = server.pmg_pbs_remote_create(
        remote="r1", datastore="ds1", server="pbs.example.com",
        encryption_key="autogen", confirm=True,
    )

    assert out["status"] == "ok"
    # reaches the caller — there is no second copy of an auto-generated key.
    assert out["result"]["config"]["encryption-key"] == _AUTOGEN_RETURN_SENTINEL

    # never recorded to the ledger.
    raw = open(log, "rb").read()
    assert _AUTOGEN_RETURN_SENTINEL.encode("utf-8") not in raw
    entry = _confirmed_entry(log, "pmg_pbs_remote_create", "ok")
    assert "result" not in entry["detail"]
    assert "config" not in entry["detail"]


