def test_remote_create_confirm_never_writes_secrets_to_ledger(tmp_path, monkeypatch):
    """weld 1: the operator's real PBS call DOES carry the raw password AND encryption-key (the
    mutation must actually work) — the fake captured both. weld 2: read the ledger file RAW
    (bytes, not parsed JSON) and assert NEITHER secret substring appears anywhere in the file."""
    _, pmg, _, log = _wire(tmp_path, monkeypatch)

    out = server.pmg_pbs_remote_create(
        remote="r1", datastore="ds1", server="pbs.example.com",
        password=_PASSWORD_SENTINEL, encryption_key=_ENCKEY_SENTINEL, confirm=True,
    )

    assert out["status"] == "ok"
    assert out["status"] != "plan"

    # weld 1: the fake captured the underlying POST with BOTH raw secrets.
    assert pmg.posts, "pmg_pbs_remote_create confirm=True never reached pmg._post"
    call_path, call_data = pmg.posts[-1]
    assert call_path == "/config/pbs"
    assert call_data["password"] == _PASSWORD_SENTINEL
    assert call_data["encryption-key"] == _ENCKEY_SENTINEL

    # weld 2: ledger entries (parsed JSON) never carry either secret sentinel.
    entries = _entries(log)
    assert entries, "no ledger entries recorded at all"
    assert all(_PASSWORD_SENTINEL not in json.dumps(e) for e in entries)
    assert all(_ENCKEY_SENTINEL not in json.dumps(e) for e in entries)
    entry = _confirmed_entry(log, "pmg_pbs_remote_create", "ok")
    assert "password" not in entry["detail"]
    assert "encryption-key" not in entry["detail"]

    # THE HEADLINE WELD: read the ledger file RAW (bytes) — neither secret appears NOWHERE.
    raw = open(log, "rb").read()
    assert _PASSWORD_SENTINEL.encode("utf-8") not in raw
    assert _ENCKEY_SENTINEL.encode("utf-8") not in raw

    # MIRROR-IMAGE: server/datastore ARE present raw in the on-disk ledger (non-secret,
    # deliberately visible).
    assert b"pbs.example.com" in raw
    assert b"ds1" in raw


