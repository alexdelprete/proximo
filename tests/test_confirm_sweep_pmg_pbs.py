class _Pmg:
    """Path-aware fake PmgBackend: records every _get/_post/_put/_delete call.

    `_get` returns a fixed, secret-FREE PBS remote config dict for `/config/pbs/r1` (the CAPTURE
    read `plan_pbs_remote_update`/`plan_pbs_remote_delete` use) and a fixed timer dict for
    `/nodes/pmg/pbs/r1/timer` (the CAPTURE read `plan_node_pbs_timer_create`/`_delete` use) —
    every other path gets `[]`/`{}` (list/dict reads default empty). `_post` returns the
    schema-confirmed ambiguous strings for snapshot create/restore/verify, and a fixed
    create-response dict for `/config/pbs`; `_put` returns a fixed update-response dict for
    `/config/pbs/r1`; `_delete` returns `None` throughout (every DELETE on this chunk's live
    schema returns `null`).
    """

    def __init__(self, remote_get_return=None, timer_get_return=None,
                 remote_create_return=None, remote_update_return=None):
        self._remote_get_return = remote_get_return or {
            "remote": "r1", "server": "pbs.example.com", "datastore": "ds1",
        }
        self._timer_get_return = timer_get_return if timer_get_return is not None else {}
        self._remote_create_return = remote_create_return or {"remote": "r1"}
        self._remote_update_return = remote_update_return or {"remote": "r1"}
        self.gets: list[tuple[str, dict | None]] = []
        self.posts: list[tuple[str, dict | None]] = []
        self.puts: list[tuple[str, dict | None]] = []
        self.deletes: list[tuple[str, dict | None]] = []

    def _get(self, path, params=None):
        self.gets.append((path, params))
        if path == "/config/pbs/r1":
            return self._remote_get_return
        if path == "/nodes/pmg/pbs/r1/timer":
            return self._timer_get_return
        return []

    def _post(self, path, data=None):
        self.posts.append((path, data))
        if path == "/config/pbs":
            return self._remote_create_return
        if path == "/nodes/pmg/pbs/r1/snapshot":
            return _SNAPSHOT_CREATE_STRING
        if path == f"/nodes/pmg/pbs/r1/snapshot/{_BACKUP_ID}/{_BACKUP_TIME}":
            return _SNAPSHOT_RESTORE_STRING
        if path == f"/nodes/pmg/pbs/r1/snapshot/{_BACKUP_ID}/{_BACKUP_TIME}/verify":
            return _VERIFY_UPID_STRING
        return None

    def _put(self, path, data=None):
        self.puts.append((path, data))
        if path == "/config/pbs/r1":
            return self._remote_update_return
        return None

    def _delete(self, path, params=None):
        self.deletes.append((path, params))
        return None


class _Api:
    """Minimal PVE API stub — only needed so server._svc() resolves (the ONE shared ledger lives
    behind it); pmg.py's/tools/pmg_mail.py's tools never touch this backend."""

    def __init__(self):
        self.config = SimpleNamespace(node="pve")


def _wire(tmp_path, monkeypatch, **pmg_kwargs):
    log = str(tmp_path / "audit.log")
    cfg = ProximoConfig(
        api_base_url="https://x:8006/api2/json", node="pve", token_path="/run/x",
        audit_log_path=log,
    )
    api = _Api()
    pmg = _Pmg(**pmg_kwargs)
    exec_ = SimpleNamespace()
    ledger = AuditLedger(log)
    monkeypatch.setattr(server, "_svc", lambda: (cfg, api, exec_, ledger))
    monkeypatch.setattr(server, "_pmg", lambda: (SimpleNamespace(node="pmg"), pmg))
    return cfg, pmg, ledger, log


def _entries(log: str) -> list[dict]:
    with open(log, encoding="utf-8") as f:
        return [json.loads(line) for line in f if line.strip()]


def _confirmed_entry(log: str, action: str, outcome: str) -> dict:
    entries = [e for e in _entries(log) if e["action"] == action and e["outcome"] == outcome]
    assert len(entries) == 1, f"expected exactly one {action!r}/{outcome!r} ledger entry, got {entries}"
    return entries[0]


